   void tblKeForm(){
        try{
            String id=inputIDUD.getText();
            Connection conn=(Connection)koneksi.koneksiDB();
            Statement stt=conn.createStatement();
            
            RsProduk=stt.executeQuery("SELECT * FROM buku WHERE id='"+id+"'");  
            while(RsProduk.next()){
                    inputIDUD.setText(RsProduk.getString("id"));
                    inputJudulUD.setText(RsProduk.getString("judul"));
                    inputGenreUD.setSelectedItem(RsProduk.getString("genre"));
                    inputPenulisUD.setText(RsProduk.getString("penulis"));
                    inputPenerbitUD.setText(RsProduk.getString("penerbit"));        
                    inputLokasiUD.setSelectedItem(RsProduk.getString("lokasi"));
                    inputStokUD.setText(RsProduk.getString("stok"));
             
            }                
        } catch (Exception ex) {
        System.err.println(ex.getMessage());
        }  
    }
