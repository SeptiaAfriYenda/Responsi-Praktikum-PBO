    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        String id=inputIDUD.getText();
        String judul=inputJudulUD.getText();
        String genre=inputGenreUD.getSelectedItem().toString();
        String penulis=inputPenulisUD.getText();
        String penerbit=inputPenerbitUD.getText();
        String lokasi=inputLokasiUD.getSelectedItem().toString();
        String stok=inputStokUD.getText();


        if (id.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"ID Buku tidak boleh kosong");
            inputIDUD.requestFocus();
        }else if (judul.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Judul buku tidak boleh kosong");
            inputJudulUD.requestFocus();
        }else if (genre.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Genre tidak boleh kosong");
            inputGenreUD.requestFocus();
        }else if (penulis.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Penulis tidak boleh kosong");
            inputPenulisUD.requestFocus();
        }
        else if (penerbit.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Penerbit tidak boleh kosong");
            inputPenerbitUD.requestFocus();
        }
        else if (lokasi.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Lokasi tidak boleh kosong");
            inputLokasiUD.requestFocus();
        }
        else if (stok.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Stok tidak boleh kosong");
            inputStokUD.requestFocus();
        }
        else{
            try{
                Connection conn=(Connection)koneksi.koneksiDB();
                Statement stt=conn.createStatement();
                stt.executeUpdate("UPDATE buku SET judul='"+judul+"', genre='"+genre+"', penulis='"+penulis+"',"+
                    "penerbit='"+penerbit+"', lokasi='"+lokasi+"', stok='"+stok+"' WHERE id='"+id+"'");
                BersihData();
                JOptionPane.showMessageDialog(this,"Data berhasil diupdate, silakan klik 'Tampilkan Semua' untuk melihat data terkini !","Success",JOptionPane.INFORMATION_MESSAGE);
            } catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Simpan data gagal\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        //exit
        dispose();
    }//GEN-LAST:event_updateActionPerformed

